## @deftypefn  {statistics} {@var{h} =} lillietest (@var{x})
## @deftypefnx {statistics} {@var{h} =} lillietest (@var{x}, @var{name}, @var{value})
## @deftypefnx {statistics} {[@var{h}, @var{p}] =} lillietest (@dots{})
## @deftypefnx {statistics} {[@var{h}, @var{p}, @var{kstat}, @var{critval}] =} lillietest (@dots{})
##
## Lilliefors goodness-of-fit hypothesis test.
##
## @code{@var{h} = lillietest (@var{x})} tests the null hypothesis that the
## sample in the vector @var{x} comes from a normal distribution with unknown
## mean and variance, against the alternative that it does not, using the
## Lilliefors test.  @var{h} is 1 if the test rejects the null at the 5%
## significance level and 0 otherwise.
##
## The Lilliefors statistic is the Kolmogorov-Smirnov statistic --- the maximum
## absolute difference between the empirical cumulative distribution function of
## @var{x} and the cumulative distribution function of the hypothesized family
## with parameters estimated from @var{x}.  Because the parameters are estimated,
## the null distribution of the statistic differs from that of the ordinary
## Kolmogorov-Smirnov test.
##
## The following @qcode{Name-Value} pairs are supported:
##
## @multitable @columnfractions 0.18 0.8
## @headitem @var{Name} @tab @var{Value}
##
## @item @qcode{'Distribution'} @tab The hypothesized family:
## @qcode{'normal'} (default), @qcode{'exponential'}, or
## @qcode{'extreme value'}.  The parameters are estimated from @var{x}: mean and
## standard deviation for the normal, mean for the exponential, and location and
## scale for the extreme value distribution.
##
## @item @qcode{'Alpha'} @tab The significance level, a scalar.  Without
## @qcode{'MCTol'} it must lie in @math{[0.001, 0.5]} (the tabulated range); with
## @qcode{'MCTol'} it may be any value in @math{(0, 1)}.  The default is
## @math{0.05}.
##
## @item @qcode{'MCTol'} @tab Maximum Monte-Carlo standard error for the
## p-value.  When supplied, the p-value and critical value are computed by
## Monte-Carlo simulation instead of by interpolating the embedded table.
## @end multitable
##
## @code{[@var{h}, @var{p}, @var{kstat}, @var{critval}] = lillietest (@dots{})}
## also returns the p-value @var{p}, the test statistic @var{kstat}, and the
## critical value @var{critval}.  Without @qcode{'MCTol'} the p-value is clamped
## to the tabulated range @math{[0.001, 0.5]} and a warning is issued when it
## lies outside.
##
## @seealso{kstest, adtest, jbtest}
## @end deftypefn

function [h, p, kstat, critval] = lillietest (x, varargin)

  if (nargin < 1)
    print_usage ();
  endif
  if (! (isnumeric (x) && isreal (x) && isvector (x)))
    error ("lillietest: X must be a vector of real values.");
  endif
  x = x(! isnan (x));
  x = x(:);
  n = numel (x);
  if (n < 4)
    error ("lillietest: X must contain at least four non-missing values.");
  endif

  ## Defaults and Name-Value parsing
  distribution = "normal";
  alpha = 0.05;
  mctol = [];
  if (mod (numel (varargin), 2) != 0)
    error ("lillietest: optional arguments must be Name-Value pairs.");
  endif
  for k = 1:2:numel (varargin)
    if (! ischar (varargin{k}))
      error ("lillietest: parameter names must be character vectors.");
    endif
    switch (lower (varargin{k}))
      case "distribution"
        distribution = lower (varargin{k+1});
      case "alpha"
        alpha = varargin{k+1};
      case "mctol"
        mctol = varargin{k+1};
      otherwise
        error ("lillietest: unknown parameter name '%s'.", varargin{k});
    endswitch
  endfor
  if (strcmp (distribution, "ev"))
    distribution = "extreme value";
  endif
  if (! any (strcmp (distribution, {"normal", "exponential", "extreme value"})))
    error ("lillietest: unrecognised 'Distribution' value.");
  endif
  domc = ! isempty (mctol);
  if (! (isnumeric (alpha) && isscalar (alpha) && isreal (alpha) ...
         && alpha > 0 && alpha < 1))
    error ("lillietest: 'Alpha' must be a scalar in the range (0,1).");
  endif
  if (! domc && (alpha < 0.001 || alpha > 0.5))
    error ("lillietest: 'Alpha' must be in [0.001, 0.5] unless 'MCTol' is set.");
  endif
  if (domc && ! (isnumeric (mctol) && isscalar (mctol) && isreal (mctol) ...
                 && mctol > 0))
    error ("lillietest: 'MCTol' must be a positive scalar.");
  endif

  ## Lilliefors (Kolmogorov-Smirnov) statistic with estimated parameters
  kstat = lillietest_stat_ (x, distribution);

  if (domc)
    ## Monte-Carlo p-value and critical value.  REPS is chosen so the worst-case
    ## standard error of the estimated p-value (at p = 0.5) is below MCTOL.
    reps = max (1000, ceil (0.25 / mctol ^ 2));
    sim = lillietest_simulate_ (n, distribution, reps);
    p = (1 + sum (sim >= kstat)) / (reps + 1);
    critval = quantile (sim, 1 - alpha);
  else
    ## Interpolate the p-value and critical value from the embedded tables.
    [p, critval] = lillietest_interp_ (kstat, n, alpha, distribution);
  endif

  h = double (kstat > critval);

endfunction

## Kolmogorov-Smirnov statistic of X against DISTRIBUTION fitted to X.
function ks = lillietest_stat_ (x, distribution)
  x = sort (x(:));
  n = numel (x);
  switch (distribution)
    case "normal"
      cdf = normcdf ((x - mean (x)) / std (x));
    case "exponential"
      cdf = expcdf (x, mean (x));
    case "extreme value"
      [mu, sigma] = lillietest_evfit_ (x);
      cdf = evcdf (x, mu, sigma);
  endswitch
  ks = max (max ((1:n)' / n - cdf), max (cdf - (0:n-1)' / n));
endfunction

## Interpolate the p-value P and critical value CRITVAL for an observed
## Lilliefors statistic KSTAT at sample size N and level ALPHA from the embedded
## tables for DISTRIBUTION.  Critical values scale roughly as 1/sqrt(N), so the
## size interpolation is carried out on sqrt(N)*CV, which is nearly flat in N.
function [p, critval] = lillietest_interp_ (kstat, n, alpha, distribution)
  [sizes, alphas, CV] = lillietest_table_ (distribution);
  ## Critical values at the ALPHAS grid for this N (scaled size interpolation).
  S = sqrt (sizes(:)) .* CV;
  nn = min (max (n, sizes(1)), sizes(end));
  cvrow = interp1 (sizes, S, nn, "linear") / sqrt (n);
  ## Critical value at the requested ALPHA.
  critval = interp1 (alphas, cvrow, alpha, "linear");
  ## P-value: invert the (decreasing) CVROW-versus-ALPHA relation, interpolating
  ## on a logarithmic ALPHA axis.  Outside the tabulated range clamp and warn.
  xa = fliplr (cvrow);
  ya = fliplr (log (alphas));
  if (kstat >= xa(end))
    warning ("lillietest:pTooSmall", ...
             "lillietest: P is less than the smallest tabulated value; returning 0.001.");
    p = 0.001;
  elseif (kstat <= xa(1))
    warning ("lillietest:pTooBig", ...
             "lillietest: P is greater than the largest tabulated value; returning 0.5.");
    p = 0.5;
  else
    p = exp (interp1 (xa, ya, kstat, "linear"));
  endif
endfunction

