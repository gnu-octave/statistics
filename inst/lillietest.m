## Simulate REPS Lilliefors statistics for samples of size N from the (pivotal)
## standardized DISTRIBUTION, evaluated in chunks to bound memory use.
function sim = lillietest_simulate_ (n, distribution, reps)
  sim = zeros (reps, 1);
  chunk = max (1, floor (2e5 / n));
  done = 0;
  ii = (1:n)' / n;
  im = (0:n-1)' / n;
  while (done < reps)
    m = min (chunk, reps - done);
    switch (distribution)
      case "normal"
        z = randn (n, m);
        z = sort (z);
        c = normcdf ((z - mean (z)) ./ std (z));
      case "exponential"
        z = -log (rand (n, m));
        z = sort (z);
        c = 1 - exp (-z ./ mean (z));
      case "extreme value"
        z = log (-log (rand (n, m)));         ## standard EV (minima) draws
        z = sort (z);
        [mu, sigma] = lillietest_evfit_ (z);  ## column-wise fit
        c = 1 - exp (-exp ((z - mu) ./ sigma));
    endswitch
    sim(done+1:done+m) = max (max (ii - c), max (c - im));
    done += m;
  endwhile
endfunction

## Maximum-likelihood fit of the extreme value (minima Gumbel) distribution,
## vectorised over the columns of X.  The scale SIGMA solves the monotone score
## equation  g(sigma) = E_w[x] - xbar - sigma = 0,  where the weights w_i are
## proportional to exp (x_i / sigma).  It is found by bisection: g is positive
## as sigma -> 0 (E_w[x] -> max (x)) and negative for large sigma, so the root
## is unique and bracketed.  A fixed-point iteration on the same equation is not
## contractive for all samples and diverges on a small fraction of them, so
## bisection is used here.  Returns row vectors MU and SIGMA.
function [mu, sigma] = lillietest_evfit_ (x)
  xbar = mean (x);
  lo = 1e-8 * ones (1, columns (x));
  hi = (max (x) - xbar) + 10;
  for iter = 1:80
    sigma = 0.5 * (lo + hi);
    mx = max (x);
    w = exp ((x - mx) ./ sigma);
    g = sum (x .* w) ./ sum (w) - xbar - sigma;
    pos = g > 0;
    lo(pos) = sigma(pos);
    hi(! pos) = sigma(! pos);
  endfor
  sigma = 0.5 * (lo + hi);
  mx = max (x);
  w = exp ((x - mx) ./ sigma);
  mu = mx + sigma .* log (sum (w) / rows (x));
endfunction

